package com.java.basic;

public class PrimitiveDataType {

	/*
	 * Primitive: default defined data types. 
	 * there are 8 primitive data types.
	 * What are different primitive data types?
	 * byte, boolean, char, int, long, short, double, float,
	 
	 * Non Primitive - anything other than primitive
	 */
	public static void main(String[] args) {
		boolean a = true;
		char b = 'A';
		int c = 5;
		short d = 6;
		long e = 78L;
		float f = 10.0f;
		double g = 12.578548d;
		byte h = 4;
		System.out.println("boolean type:" + a);
		System.out.println("char type:" + b);
		System.out.println("integer type " + c);
		System.out.println("short type" + d);
		System.out.println("long type:" + e);
		System.out.println("float type:" + f);
		System.out.println("double type:" + g);
		System.out.println("byte type:" + h);

	}
}
