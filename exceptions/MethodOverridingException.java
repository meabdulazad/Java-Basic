package com.java.exceptions;

public class MethodOverridingException {

	/*
	 * No rules for method overloading.
	 * Rules of Exception for MethodOverriding:
		 * If SuperClass does not declare an exception, then the SubClass can only
		 * declare unchecked exceptions (RunTime), but not the checked exceptions. 
		 * If SuperClass declares an exception, then the SubClass can only declare the same or child
		 * exceptions of the exception declared by the SuperClass and any new Runtime
		 * Exceptions, just not any new checked exceptions at the same level or higher.
		 * If SuperClass declares an exception, then the SubClass can declare without
		 * exception.
	 */
	
	    
	   
}
