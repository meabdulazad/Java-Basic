	//OOPs: Object-Oriented Programming is a paradigm that provides many concepts like inheritance, data binding, polymorphism, etc.
	// Simula is considered as the first OOP language.
	// Smalltalk is considered the first truly (100%) oop language.
	// Popular OOP language are Java, C#, PHP, Python, C++, etc.
	
		// Object-Oriented Programming is a methodology or paradigm to design a program using classes and objects.
	
	
	// Some concepts: Object, Class, Inheritance, Polymorphism, Abstraction, Encapsulation.
		// Object: Any entity with state and behavior. eg: chair, car, desk.
		// Class: Collection of objects. It is a logical entity. Like a blueprint from where you can create an object. Class doesn't consume any space.
		// Inheritance: Acquires all the properties and behavior of a parent object. Termed as IS-A relationship. Parent-child relationship.
		// Polymorphism: one task performed in different ways. eg: to draw something like shapes, triangle, square, etc.
		// Abstraction: Hiding internal details and showing functionality. eg: phone call as we don't know internal processing.
		// Encapsulation: Binding or wrapping codes and data together into a single unit. eg: a capsule wrapped with different medicines. Java class is an example of encapsulation.
	 
	
	// Some other terms used in Object-Oriented design: Coupling, Cohesion, Association, Aggregation, Composition.
		// Coupling: Refers to the knowledge or information or dependency of another class. Strong coupling, if a class has the detail information of another class.
		// Cohesion: Refers to the level of component which performs a single well-defined task. A weekly cohesive method will split the task in separate parts whereas strong cohesive method perform single well-defined task. eg: java.io package is highly cohesive package as it has I/O related classes and interface. java.util package is weakly cohesive package as it has unrelated classes and interfaces.
		// Association: Represents the relationship between objects. There can be 4 types of association between objects i.e. One to One, One to Many, Many to One, Many to Many.
		// Aggregation: A way to achieve Association. Termed as has-a relationship. Used for code re-usability. eg: Car has a engine, TV has a screen. Car-cover is an example.
		// Composition: A way to achieve Association like aggregation. Termed as part-of relationship. eg: Battery is part of Laptop. 
	
